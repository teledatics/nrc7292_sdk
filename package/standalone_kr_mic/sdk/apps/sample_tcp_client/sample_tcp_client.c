/****************************************************************************
 * FunctionName : open_connection
 * Description  : create tcp sockets and connect to server
 * Parameters   : void
 * Returns	    : true or false
 *****************************************************************************/
static bool open_connection(void)
{
	int ret =0;

	sockfd = socket(AF_INET, SOCK_STREAM, IPPROTO_TCP);
	if (sockfd < 0) {
		nrc_usr_print("Create socket failed\n");
		return false;
	}

	ret =connect(sockfd, (struct sockaddr *)&dest_addr, sizeof(dest_addr));
	if (ret < 0) {
		nrc_usr_print("open_connection failed\n");
		close(sockfd);
		sockfd = -1;
		return false;
	}

	return true;
}

/****************************************************************************
 * FunctionName : close_connection
 * Description  : shutdown connection and close socket
 * Parameters   : void
 * Returns	    : true
 *****************************************************************************/
static bool close_connection(void)
{
	if(sockfd >= 0) {
		nrc_usr_print("close_connection :%d\n", sockfd);
		shutdown(sockfd, SHUT_RDWR);
		close(sockfd);
		sockfd = -1;
	}
	return true;
}

/****************************************************************************
 * FunctionName : retry_tcp_connection
 * Description  : close connection and open connection again
 * Parameters   : void
 * Returns	    : true or false
 *****************************************************************************/
static bool retry_tcp_connection(void)
{
	int bSuccess = true;

	bSuccess = close_connection();
	if (bSuccess == false) {
		return false;
	}

	bSuccess = open_connection();
	if (bSuccess == false) {
		return false;
	}

	nrc_usr_print("retry_tcp_connection success!!! \n");
	return true;
}

/****************************************************************************
 * FunctionName : send_socket_data
 * Description  : close connection and open connection again
 * Parameters   : char* data      - data pointer
 *                int data_length - data length
 * Returns	    : true or false
 *****************************************************************************/
static bool send_socket_data(char* data, int data_length)
{
	int send_retry_count = 0;
	int send_len = 0;
	int remain_len = data_length;

	while (remain_len > 0) {
		send_len = send(sockfd, data, remain_len, 0);
		if (send_len < 0) {
			if(send_retry_count <  MAX_SEND_RERTY){
				send_retry_count++;
				_delay_ms(1000);
				continue;
			}else{
				return false;
			}
		}
		send_retry_count = 0;
		data		+= send_len;
		remain_len	-= send_len;
	}
	return true;
}

